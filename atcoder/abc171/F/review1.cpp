using namespace std;
template<class T = Mint>
struct Factorials {
  // factorials and inverse factorials.
  std::vector<T> fact, ifact;

  // n: max cached value.
  Factorials(int n) : fact(n + 1), ifact(n + 1) {
    assert(n >= 0);
    assert(n < T::mod());
    fact[0] = 1;
    for (int i = 1; i <= n; ++i) {
      fact[i] = fact[i - 1] * i;
    }
    ifact[n] = fact[n].inv();
    for (int i = n; i >= 1; --i) {
      ifact[i - 1] = ifact[i] * i;
    }
  }

  // Combination (nCk)
  T C(int n, int k) const {
    if (k < 0 || k > n) return 0;
    return fact[n] * ifact[k] * ifact[n - k];
  }
};

template<typename T>
std::vector<T> pow_seq(int n, int base = 2) {
  assert(n >= 0 and base > 0);
  std::vector<T> p(n + 1);
  p[0] = 1;
  for (int i = 0; i < n; ++i) {
    p[i + 1] = p[i] * base;
  }
  return p;
}

auto solve() {
  int K = in;
  string S = in;
  int n = ssize(S);
  Factorials<Mint> fs(n + K);
  auto pow25 = pow_seq<Mint>(K, 25);
  auto pow26 = pow_seq<Mint>(K, 26);
  Mint ans;
  for (int i = 0; i <= K; ++i) {
    Mint now = pow26[i];
    now *= fs.C(n - 1 + K - i, n - 1);
    now *= pow25[K - i];
    ans += now;
  }
  return ans;
}

