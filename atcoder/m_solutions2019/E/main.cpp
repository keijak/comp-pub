using namespace std;

template <class T = Mint>
struct Factorials {
  // factorials and inverse factorials.
  std::vector<T> fact, ifact;

  // n: max cached value.
  Factorials(int n) : fact(n + 1), ifact(n + 1) {
    assert(n > 0 and n < T::mod());
    fact[0] = 1;
    for (int i = 1; i <= n; ++i) fact[i] = fact[i - 1] * i;
    ifact[n] = fact[n].inv();
    for (int i = n; i >= 1; --i) ifact[i - 1] = ifact[i] * i;
  }
};

int main() {
  ios_base::sync_with_stdio(false), cin.tie(nullptr);
  Factorials fs(M - 1);

  auto solve = [&]() -> Mint {
    int x, d, n;
    cin >> x >> d >> n;

    if (d == 0) {
      return Mint(x).pow(n);
    }
    {
      Mint k = Mint(M - x) / Mint(d);
      if (n - 1 >= (int)k.val()) return 0;
    }
    int r = (Mint(x) / d).val();
    if (r == 0 or r + n - 1 >= M) return 0;
    Mint q = fs.fact[r + n - 1] * fs.ifact[r - 1];
    return q * Mint(d).pow(n);
  };

  int q;
  cin >> q;
  REP(i, q) { cout << solve() << "\n"; }
}
