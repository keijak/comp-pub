using namespace std;

Mint solve() {
  int n, a, b, c;
  cin >> n >> a >> b >> c;
  if (a < b) swap(a, b);
  Factorials fs(2 * n);

  Mint pa = Mint(a) / (a + b);
  Mint pb = Mint(b) / (a + b);
  Mint pan = pa.pow(n), pbn = pb.pow(n);
  Mint pak = 1, pbk = 1;
  Mint expected = 0;
  for (int k = n; k <= 2 * n - 1; ++k) {
    auto co = fs.C(k - 1, n - 1) * k;
    expected += co * (pbk * pan + pak * pbn);
    pbk *= pb;
    pak *= pa;
  }
  Mint one_step = Mint(100) / (100 - c);
  return expected * one_step;
}

