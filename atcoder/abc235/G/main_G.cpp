template<class T = Mint>
struct Factorials {
  // factorials and inverse factorials.
  std::vector<T> fact, ifact;

  // n: max cached value.
  Factorials(int n) : fact(n + 1), ifact(n + 1) {
    assert(n >= 0);
    assert(n < T::mod());
    fact[0] = 1;
    for (int i = 1; i <= n; ++i) {
      fact[i] = fact[i - 1] * i;
    }
    ifact[n] = fact[n].inv();
    for (int i = n; i >= 1; --i) {
      ifact[i - 1] = ifact[i] * i;
    }
  }

  // Combination (binomial coefficients)
  T C(Int n, Int k) const {
    if (k < 0 || k > n) return 0;
    return fact[n] * ifact[k] * ifact[n - k];
  }
};

using namespace std;

auto solve() {
  int n = in, A = in, B = in, C = in;
  Factorials fs(n + 10);
  Mint pa = 1, pb = 1, pc = 1;

  Mint ans = 0;
  for (int k = n; k >= 0; --k) {
    Mint now = fs.C(n, k);
    now *= pa;
    now *= pb;
    now *= pc;
    if (k & 1) {
      ans -= now;
    } else {
      ans += now;
    }

    int m = n - k;
    if (A < m + 1) {
      pa = 2 * pa - fs.C(m, A);
    } else {
      pa *= 2;
    }
    if (B < m + 1) {
      pb = 2 * pb - fs.C(m, B);
    } else {
      pb *= 2;
    }
    if (C < m + 1) {
      pc = 2 * pc - fs.C(m, C);
    } else {
      pc *= 2;
    }
  }
  return ans;
}

