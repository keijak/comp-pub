template<class F>
struct Rec {
  F f_;
  explicit Rec(F f) : f_(std::move(f)) {}
  template<class... Args>
  decltype(auto) operator()(Args &&... args) {
    return f_(*this, std::forward<Args>(args)...);
  }
};
template<class F> Rec(F) -> Rec<F>;

auto solve() {
  int n = in;
  vector<int> P = in(n), Q = in(n);
  REP(i, n) {
    P[i]--, Q[i]--;
  }
  vector<int> R(n);
  REP(i, n) {
    R[P[i]] = Q[i];
  }
  Mint ans = 1;
  vector<int> done(n, 0);
  auto f = Rec([&](auto &self, int i, array<Mint, 2> val, int cycle_len, bool ssel) -> Mint {
    if (i == cycle_len - 1) {
      if (ssel) {
        return val[0] + val[1] + val[1];
      } else {
        return val[0] + val[1];
      }
    }
    return self(i + 1, array<Mint, 2>{val[1], val[0] + val[1]}, cycle_len, ssel);
  });
  REP(i, n) {
    if (done[i]) continue;
    int cycle_len = 0;
    for (int v = i; not done[v]; v = R[v]) {
      done[v] = true;
      ++cycle_len;
    }
    if (cycle_len == 1) continue;
    Mint co1 = f(1, array<Mint, 2>{1, 0}, cycle_len, false);
    Mint co2 = f(1, array<Mint, 2>{0, 1}, cycle_len, true);
    DUMP(i, cycle_len, co1.val(), co2.val());
    ans *= co1 + co2;
  }
  print(ans);
}

