using namespace std;
template <typename Monoid>
struct SegTree {
  using T = typename Monoid::T;

  inline int n() const { return n_; }
  inline int offset() const { return offset_; }

  explicit SegTree(int n) : n_(n) {
    offset_ = 1;
    while (offset_ < n_) offset_ <<= 1;
    data_.assign(2 * offset_, Monoid::id());
  }

  explicit SegTree(const std::vector<T> &leaves) : n_(leaves.size()) {
    offset_ = 1;
    while (offset_ < n_) offset_ <<= 1;
    data_.assign(2 * offset_, Monoid::id());
    for (int i = 0; i < n_; ++i) {
      data_[offset_ + i] = leaves[i];
    }
    for (int i = offset_ - 1; i > 0; --i) {
      data_[i] = Monoid::op(data_[i * 2], data_[i * 2 + 1]);
    }
  }

  // Sets i-th value (0-indexed) to x.
  void set(int i, const T &x) {
    int k = offset_ + i;
    data_[k] = x;
    // Update its ancestors.
    while (k > 1) {
      k >>= 1;
      data_[k] = Monoid::op(data_[k * 2], data_[k * 2 + 1]);
    }
  }

  // Queries by [l,r) range (0-indexed, half-open interval).
  T fold(int l, int r) const {
    l = std::max(l, 0) + offset_;
    r = std::min(r, offset_) + offset_;
    T vleft = Monoid::id(), vright = Monoid::id();
    for (; l < r; l >>= 1, r >>= 1) {
      if (l & 1) vleft = Monoid::op(vleft, data_[l++]);
      if (r & 1) vright = Monoid::op(data_[--r], vright);
    }
    return Monoid::op(vleft, vright);
  }

  T fold_all() const { return data_[1]; }

  friend std::ostream &operator<<(std::ostream &os, const SegTree &st) {
    os << "[";
    for (int i = 0; i < st.n(); ++i) {
      if (i != 0) os << ", ";
      const auto &x = st[i];
      os << x;
    }
    return os << "]";
  }

 private:
  int n_;                // number of valid leaves.
  int offset_;           // where leaves start
  std::vector<T> data_;  // data size: 2*offset_
  // std::unordered_map<int, T> data_;  // data size: 2*offset_
};

using Float = double;
struct ax_b {
  struct T {
    Float a;
    Float b;
    T() : a(1.0), b(0.0) {}
    T(Float a, Float b) : a(a), b(b) {}
  };
  static T op(const T &x, const T &y) { return {y.a * x.a, y.a * x.b + y.b}; }
  static T id() { return {1, 0}; }
};

template <typename T>
struct Compress {
  std::vector<T> vec;

  explicit Compress(std::vector<T> v) : vec(v) {
    std::sort(vec.begin(), vec.end());
    vec.erase(std::unique(vec.begin(), vec.end()), vec.end());
  }

  int size() const { return vec.size(); }

  int index(T x) const {
    return lower_bound(vec.begin(), vec.end(), x) - vec.begin();
  }

  const T &value(int i) const { return vec[i]; }
};

array<Float, 2> solve() {
  i64 n, m;
  cin >> n >> m;

  vector<i64> coords(m);
  vector<tuple<i64, Float, Float>> inputs(m);
  REP(i, m) {
    i64 p;
    Float a, b;
    cin >> p >> a >> b;
    inputs[i] = {p, a, b};
    coords[i] = p;
  }

  Compress<i64> comp(move(coords));
  SegTree<ax_b> seg(comp.size());
  Float mn = 1, mx = 1;
  for (auto [p, a, b] : inputs) {
    int pi = comp.index(p);
    seg.set(pi, {a, b});
    auto res = seg.fold_all();
    Float val = res.a + res.b;
    chmin(mn, val);
    chmax(mx, val);
    DEBUG(p, a, b, res.a, res.b, val, mn, mx);
  }
  return {mn, mx};
}

int main() {
  std::ios::sync_with_stdio(false), cin.tie(nullptr);
  cout << std::fixed << std::setprecision(8);
  auto [mn, mx] = solve();
  cout << mn << endl << mx << endl;
}
