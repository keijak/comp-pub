using namespace std;

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int N, C;
  cin >> N >> C;
  V<V<pair<int, int>>> chan(C);

  REP(i, N) {
    int s, t, c;
    cin >> s >> t >> c;
    c--;
    chan[c].emplace_back(s, t);
  }
  REP(c, C) {
    sort(ALL(chan[c]));
    V<pair<int, int>> q;
    for (auto [s, t] : chan[c]) {
      if (q.empty()) {
        q.emplace_back(s, t);
      } else if (q.back().second == s) {
        q[q.size() - 1] = {q.back().first, t};
      } else {
        q.emplace_back(s, t);
      }
    }
    swap(q, chan[c]);
  }

  const int TL = 100'005;
  V<int> timeline(TL + 1);
  REP(c, C) {
    for (auto [s, t] : chan[c]) {
      timeline[s - 1]++;
      timeline[t]--;
    }
  }
  for (int i = 1; i <= TL; ++i) {
    timeline[i] += timeline[i - 1];
  }
  cout << *max_element(ALL(timeline)) << endl;
  ;
}
