auto solve() {
  string S = in;
  string T = "";
  auto char_match = [&](char c1, char c2) -> bool {
    if (c2 == '.') return true;
    return c1 == c2;
  };
  auto check = [&]() -> bool {
    REP(i, ssize(S)) {
      if (i + ssize(T) > ssize(S)) break;
      bool ok = true;
      REP(j, ssize(T)) {
        if (not char_match(S[i + j], T[j])) ok = false;
      }
      if (ok) {
        return true;
      }
    }
    return false;
  };
  int cnt = 0;
  for (int i = 0; i <= 26; ++i) {
    char c1 = (i != 26) ? ('a' + i) : '.';
    T = string(1, c1);
    if (check()) ++cnt;
    for (int j = 0; j <= 26; ++j) {
      char c2 = (j != 26) ? ('a' + j) : '.';
      T = string(1, c1) + string(1, c2);
      if (check()) ++cnt;
      T.push_back(' ');
      for (int k = 0; k <= 26; ++k) {
        char c3 = (k != 26) ? ('a' + k) : '.';
        T[2] = c3;
        if (check()) ++cnt;
      }
    }
  }
  return cnt;
}

