using namespace std;

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int R, C, K, N;
  cin >> R >> C >> K >> N;
  V<pair<int, int>> candies(N);
  map<int, V<int>> rows, cols;
  REP(i, N) {
    int r, c;
    cin >> r >> c;
    r--;
    c--;
    candies[i] = {r, c};
    rows[r].push_back(i);
    cols[c].push_back(i);
  }

  V<i64> col_freq(N + 1);
  for (const auto &[c, indices] : cols) {
    col_freq[SIZE(indices)]++;
  }
  col_freq[0] = C - SIZE(cols);
  i64 num_zero_rows = R - SIZE(rows);

  u64 ans = num_zero_rows * col_freq[K];
  for (const auto &[r, indices] : rows) {
    int k = SIZE(indices);
    if (k > K) continue;
    i64 tc0 = K - k;
    i64 tc1 = K - k + 1;
    i64 freq = col_freq[tc0];
    for (auto i : indices) {
      int c = candies[i].second;
      int f = SIZE(cols[c]);
      if (f == tc0) --freq;
      if (f == tc1) ++freq;
    }
    ans += freq;
  }
  cout << ans << endl;
}
