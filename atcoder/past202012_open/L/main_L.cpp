auto solve() {
  int n = in;
  string S = in;
  string T = in;
  auto dp = vector(n + 1, vector(n + 1, 0));
  for (int r = 0; r <= n; ++r) {
    for (int l = r; l >= 0; --l) {
      for (int m = l + 1; m <= r - 1; ++m) {
        chmax(dp[l][r], dp[l][m] + dp[m][r]);
      }
      for (int m = l + 1; m < r - 1; ++m) {
        if (S[m] != T[1]) continue;
        bool l_ok = false;
        for (int li = m - 1; li >= l; --li) {
          if (S[li] != T[0]) continue;
          if (dp[li + 1][m] == (m - (li + 1)) and dp[l][li] == (li - l)) {
            l_ok = true;
            break;
          }
        }
        bool r_ok = false;
        for (int ri = m + 1; ri < r; ++ri) {
          if (S[ri] != T[2]) continue;
          if (dp[m + 1][ri] == (ri - (m + 1)) and dp[ri + 1][r] == (r - (ri + 1))) {
            r_ok = true;
            break;
          }
        }
        if (l_ok and r_ok) {
          chmax(dp[l][r], r - l);
        }
      }
    }
  }
  check(dp[0][n] % 3 == 0);
  return dp[0][n] / 3;
}

