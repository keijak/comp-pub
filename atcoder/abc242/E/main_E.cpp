template<typename T>
std::vector<T> pow_seq(int n, int base = 2) {
  assert(n >= 0 and base > 0);
  std::vector<T> p(n + 1);
  p[0] = 1;
  for (int i = 0; i < n; ++i) {
    p[i + 1] = p[i] * base;
  }
  return p;
}

Mint solve(int tcase) {
  int n = in;
  string S = in;
  assert(ssize(S) == n);
  bool s_odd = n % 2 == 1;
  int half = n / 2;
  auto pow26 = pow_seq<Mint>(half + 100, 26);

  Mint ans = 0;
  REP(i, half) {
    int smaller = (S[i] - 'A');
    int t = half - i - 1 + int(s_odd);
    ans += smaller * pow26[t];
  }
  if (s_odd) {
    ans += S[half] - 'A';
  }
  string T(S.begin(), S.begin() + (half + int(s_odd)));
  for (int i = half - 1; i >= 0; --i) {
    T.push_back(S[i]);
  }
  if (T <= S) {
    ans += 1;
  }
  return ans;
}

