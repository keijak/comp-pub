int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int N;
  cin >> N;
  V<int> A(N), B(N);
  cin >> A >> B;
  reverse(WHOLE(B));
  int dupnum = -1, l = -1, r = -1;
  REP(i, N) {
    if (A[i] == B[i]) {
      assert(dupnum == -1 or dupnum == A[i]);
      dupnum = A[i];
      if (l == -1) l = i;
      r = i;
    }
  }
  if (dupnum != -1) {
    int head = 0, tail = N - 1;
    for (int i = l; i <= r; ++i) {
      if (B[head] != dupnum and A[head] != dupnum) {
        swap(B[head], B[i]);
        ++head;
      } else if (B[tail] != dupnum and A[tail] != dupnum) {
        swap(B[tail], B[i]);
        --tail;
      } else {  // Impossible.
        cout << "No\n";
        exit(0);
      }
    }
  }
  cout << "Yes\n";
  pprint(B);
}
