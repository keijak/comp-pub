using namespace std;
using Real = long double;

template <int sign = 1>
struct Infinity {
  template <typename T>
  constexpr operator T() const {
    static_assert(sign == 1 or not std::is_unsigned_v<T>,
                  "must be positive in an unsigned type");
    if constexpr (std::numeric_limits<T>::has_infinity) {
      return T(sign) * std::numeric_limits<T>::infinity();
    } else {
      return T(sign) * (std::numeric_limits<T>::max() / T(2));
    }
  }
  constexpr Infinity<sign * -1> operator-() const { return {}; }
  template <typename T>
  friend bool operator==(const T &x, const Infinity &y) {
    return x == T(y);
  }
  template <typename T>
  friend bool operator!=(const T &x, const Infinity &y) {
    return x != T(y);
  }
};
constexpr Infinity<> INF;

auto solve() {
  int n;
  cin >> n;
  vector<i64> a(n);
  REP(i, n) { cin >> a[i]; }
  sort(ALL(a));
  const i64 t = a[n / 2];
  DUMP(t);

  Real ans = 0;
  REP(i, n) {
    if (t < a[i]) {
      ans += -0.5 * t + a[i];
    } else {
      ans += 0.5 * t;
    }
  }
  ans /= n;
  return ans;
}

int main() {
  ios_base::sync_with_stdio(false), cin.tie(nullptr);
  cout << std::fixed << std::setprecision(15);
  cout << solve() << "\n";
}
