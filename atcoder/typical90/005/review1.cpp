template<class F>
struct Rec {
  F f_;
  explicit Rec(F f) : f_(std::move(f)) {}
  template<class... Args>
  decltype(auto) operator()(Args &&... args) {
    return f_(*this, std::forward<Args>(args)...);
  }
};
template<class F> Rec(F) -> Rec<F>;

using namespace std;

auto solve() {
  const Int N = in;
  const int B = in, K = in;
  const vector<int> C = in(K);
  auto ans = Rec([&](auto &f, Int n) -> vector<Mint> {
    auto ret = vector<Mint>(B, 0);
    if (n == 0) {
      ret[0] = 1;
      return ret;
    }
    if (n & 1) {
      auto s = f(n - 1);
      REP(i, B) {
        for (auto c: C) {
          ret[(i * 10 + c) % B] += s[i];
        }
      }
    } else {
      auto s = f(n / 2);
      vector<Mint> t(B, 0);
      REP(i, B) {
        Int j = atcoder::pow_mod(10, n / 2, B);
        t[(i * j) % B] += s[i];
      }
      REP(i, B) {
        REP(j, B) {
          ret[(i + j) % B] += t[i] * s[j];
        }
      }
    }
    return ret;
  })(N);
  DUMP(ans);
  print(ans[0].val());
}

