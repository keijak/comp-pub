template<class F>
struct Rec {
  F f_;
  explicit Rec(F f) : f_(std::move(f)) {}
  template<class... Args>
  decltype(auto) operator()(Args &&... args) {
    return f_(*this, std::forward<Args>(args)...);
  }
};
template<class F> Rec(F) -> Rec<F>;

using BS = bitset<88>;

auto solve() {
  int n = in, Q = in;
  vector<int> a = in(n);
  vector<BS> ban(n);
  REP(i, Q) {
    int x = in, y = in;
    --x, --y;
    ban[y].set(x);
  }
  map<int, BS> seen;
  BS buf;

  auto to_vec = [](const BS &b) {
    vector<int> ret;
    for (int i = 0; i < 88; ++i) {
      if (b[i]) ret.push_back(i + 1);
    }
    return ret;
  };

  Rec([&](auto self, int i, int s) -> void {
    if (i == n) {
      return;
    }
    self(i + 1, s);
    if ((ban[i] & buf).any()) {
      return;
    }
    buf[i] = true;
    if (auto it = seen.find(s + a[i]); it != seen.end()) {
      auto v1 = to_vec(it->second);
      auto v2 = to_vec(buf);
      print(v1.size());
      print_seq(v1);
      print(v2.size());
      print_seq(v2);
      exit_();
    }
    seen[s + a[i]] = buf;
    self(i + 1, s + a[i]);
    buf[i] = false;
  })(0, 0);
  assert(false);
}

