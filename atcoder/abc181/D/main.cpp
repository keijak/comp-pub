using namespace std;

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  string s;
  cin >> s;
  int n = s.size();
  map<int, int> c;
  REP(i, n) {
    int d = s[i] - '0';
    c[d]++;
  }
  bool ok = [&]() -> bool {
    if (n < 4) {
      string t = s;
      sort(ALL(t));
      do {
        u64 y;
        stringstream ss;
        ss << t;
        ss >> y;
        if (y % 8 == 0) return true;
      } while (next_permutation(ALL(t)));
      return false;
    }
    if (c[0] >= 3 and n > 3) return true;

    for (i64 x = 8; x < 1000; x += 8) {
      int d1 = x % 10;
      int d2 = (x / 10) % 10;
      int d3 = (x / 100) % 10;
      c[d1]--;
      c[d2]--;
      c[d3]--;
      if (c[d1] >= 0 and c[d2] >= 0 and c[d3] >= 0) return true;
      c[d1]++;
      c[d2]++;
      c[d3]++;
    }
    return false;
  }();
  cout << (ok ? "Yes\n" : "No\n");
}
