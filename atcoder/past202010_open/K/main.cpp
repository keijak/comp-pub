template <unsigned int M>
struct ModInt {
  constexpr ModInt(long long val = 0) : _v(0) {
    if (val < 0) {
      long long k = (abs(val) + M - 1) / M;
      val += k * M;
    }
    assert(val >= 0);
    _v = val % M;
  }

  static constexpr int mod() { return M; }
  static constexpr unsigned int umod() { return M; }
  inline unsigned int val() const { return _v; }

  ModInt &operator++() {
    _v++;
    if (_v == umod()) _v = 0;
    return *this;
  }
  ModInt &operator--() {
    if (_v == 0) _v = umod();
    _v--;
    return *this;
  }
  ModInt operator++(int) {
    auto result = *this;
    ++*this;
    return result;
  }
  ModInt operator--(int) {
    auto result = *this;
    --*this;
    return result;
  }

  constexpr ModInt operator-() const { return ModInt(-_v); }
  constexpr ModInt &operator+=(const ModInt &a) {
    if ((_v += a._v) >= M) _v -= M;
    return *this;
  }
  constexpr ModInt &operator-=(const ModInt &a) {
    if ((_v += M - a._v) >= M) _v -= M;
    return *this;
  }
  constexpr ModInt &operator*=(const ModInt &a) {
    _v = ((unsigned long long)(_v)*a._v) % M;
    return *this;
  }
  constexpr ModInt pow(unsigned long long t) const {
    ModInt base = *this;
    ModInt res = 1;
    while (t) {
      if (t & 1) res *= base;
      base *= base;
      t >>= 1;
    }
    return res;
  }

  friend constexpr ModInt operator+(const ModInt &a, const ModInt &b) {
    return ModInt(a) += b;
  }
  friend constexpr ModInt operator-(const ModInt &a, const ModInt &b) {
    return ModInt(a) -= b;
  }
  friend constexpr ModInt operator*(const ModInt &a, const ModInt &b) {
    return ModInt(a) *= b;
  }
  friend constexpr bool operator==(const ModInt &a, const ModInt &b) {
    return a._v == b._v;
  }
  friend constexpr bool operator!=(const ModInt &a, const ModInt &b) {
    return a._v != b._v;
  }
  friend std::istream &operator>>(std::istream &is, ModInt &a) {
    return is >> a._v;
  }
  friend std::ostream &operator<<(std::ostream &os, const ModInt &a) {
    return os << a._v;
  }

 private:
  unsigned int _v;  // raw value
};
const unsigned int MOD = 1'000'000'000;
using Mint = ModInt<MOD>;

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int K, Q;
  cin >> K;
  V<V<int>> a(K);
  V<V<Mint>> freq(K, vector<Mint>(21, 0));
  V<Mint> inv(K, Mint(0));
  REP(i, K) {
    int n;
    cin >> n;
    a[i].resize(n);
    Mint invi = 0;
    REP(j, n) {
      cin >> a[i][j];
      for (int d = a[i][j] + 1; d <= 20; ++d) {
        invi += freq[i][d];
      }
      freq[i][a[i][j]]++;
    }
    inv[i] = invi;
  }

  DEBUG(inv);

  cin >> Q;
  V<int> b(Q);
  cin >> b;

  V<Mint> cum_freq(21, 0LL);
  Mint ans = 0;
  REP(i, Q) {
    int k = b[i] - 1;
    ans += inv[k];
    for (int d = 1; d <= 20; ++d) {
      for (int e = d + 1; e <= 20; ++e) {
        ans += freq[k][d] * cum_freq[e];
      }
    }
    for (int d = 1; d <= 20; ++d) {
      cum_freq[d] += freq[k][d];
    }
  }
  cout << ans.val() << endl;
}
