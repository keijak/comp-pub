using namespace std;

bool solve() {
  i64 a, b, c, d;
  cin >> a >> b >> c >> d;
  if (a < b or d < b) return false;
  if (c >= b - 1) return true;

  unordered_set<i64> seen;
  seen.reserve(1 << 20);
  seen.max_load_factor(0.25);
  seen.insert(a % b);
  i64 x = a % b;
  for (;;) {
    assert(x < b);
    if (x > c) return false;
    x += d;
    __int128_t k = (x - c + (b - 1)) / b;
    k *= b;
    if (k > x) return false;
    x -= k;
    if (not seen.insert(x).second) return true;
  }
  assert(false);
}

