int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int N, M;
  cin >> N >> M;
  V<i64> xs, ys;
  REP(i, M) {
    i64 x, y;
    cin >> x >> y;
    xs.emplace_back(x);
    ys.emplace_back(y);
  }
  sort(xs.begin(), xs.end());
  sort(ys.begin(), ys.end());

  auto check = [&](int width) -> bool {
    int xc = 1;
    int x = xs[0];
    for (int i = 1; i < M; ++i) {
      if (xs[i] - x > width) {
        ++xc;
        x = xs[i];
      }
    }
    int yc = 1;
    int y = ys[0];
    for (int i = 1; i < M; ++i) {
      if (ys[i] - y > width) {
        ++yc;
        y = ys[i];
      }
    }
    return xc + yc <= N;
  };

  int ng = -1, ok = max(xs[M - 1] - xs[0], ys[M - 1] - ys[0]) + 5;
  while (ok - ng > 1) {
    int mid = (ng + ok) / 2;
    (check(mid) ? ok : ng) = mid;
  }
  cout << ok << endl;
}
