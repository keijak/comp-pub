template<class T>
T floor_div(T x, T y) {
  assert(y != 0);
  return x / y - (((x ^ y) < 0) and (x % y));
}

template<class T>
T ceil_div(T x, T y) {
  assert(y != 0);
  return x / y + (((x ^ y) >= 0) and (x % y));
}

struct PrimeSieve {
  std::vector<int> spf;  // smallest prime factors table.
  std::vector<int> primes;

  explicit PrimeSieve(int n) : spf(n + 1) {
    // O(n)
    for (int i = 2; i <= n; ++i) {
      if (spf[i] == 0) {
        spf[i] = i;
        primes.push_back(i);
      }
      for (const auto &p: primes) {
        if (i * p > n or p > spf[i]) break;
        spf[i * p] = p;
      }
    }
  }

  // MÃ¶bius function.
  int moebius(int n) const {
    assert(0 < n and n < int(spf.size()));
    int res = 1;
    while (n > 1) {
      const int p = spf[n];
      n /= p;
      if (n % p == 0) return 0;
      res *= -1;
    }
    return res;
  }
};

auto solve() {
  Int L = in, R = in;
  PrimeSieve sieve(R + 1);
  vector<Int> dp(R + 1);
  Int ans = 0;
  for (Int g = R; g >= 2; --g) {
    for (Int d = 1; d <= R / g; ++d) {
      int mu = sieve.moebius(d);
      if (mu == 0) continue;
      Int s = floor_div(R, g * d) - ceil_div(L, g * d) + 1;
      ans += mu * (s * (s - 1) / 2);
    }
    if (g >= L) {
      ans -= R / g - 1;
    }
  }
  ans *= 2;
  return ans;
}

