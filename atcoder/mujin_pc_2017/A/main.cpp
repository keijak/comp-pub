using namespace std;
using Mint = atcoder::modint1000000007;

template <class T = Mint>
struct Factorials {
  // factorials and inverse factorials.
  std::vector<T> fact, ifact;

  // n: max cached value.
  Factorials(int n) : fact(n + 1), ifact(n + 1) {
    assert(n > 0 and n < T::mod());
    fact[0] = 1;
    for (int i = 1; i <= n; ++i) fact[i] = fact[i - 1] * i;
    ifact[n] = fact[n].inv();
    for (int i = n; i >= 1; --i) ifact[i - 1] = ifact[i] * i;
  }

  // Combination (nCk)
  T C(int n, int k) {
    if (k < 0 || k > n) return 0;
    return fact[n] * ifact[k] * ifact[n - k];
  }

  // Permutation (nPk)
  T P(int n, int k) {
    if (k < 0 || k > n) return 0;
    return fact[n] * ifact[n - k];
  }
};

i64 solve() {
  int n;
  cin >> n;
  vector<int> X(n);
  cin >> X;
  Factorials<Mint> fs(n);

  Mint ans = 1;
  int cur = 1;
  int k = 0;
  REP(i, n) {
    ++k;
    if (cur <= X[i]) {
      X[i] = cur;
      cur += 2;
    } else {
      assert(X[i] == cur - 1);
      ans *= k;
      --k;
    }
  }
  ans *= fs.fact[k];
  return ans.val();
}

int main() {
  std::ios::sync_with_stdio(false), cin.tie(nullptr);
  cout << solve() << "\n";
}
