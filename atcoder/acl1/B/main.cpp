using namespace std;

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  i64 N;
  cin >> N;
  i64 N2 = N * 2;
  i64 ans = 1e18;
  for (i64 x = 1; x * x <= N2; ++x) {
    if (N2 % x != 0) continue;
    i64 y = N2 / x;

    V<i64> r1 = {0, y - 1}, m1 = {x, y};
    auto [y1, z1] = atcoder::crt(r1, m1);
    if (z1 != 0) {
      if (y1 == 0) y1 = z1;
      chmin(ans, y1);
    }

    V<i64> r2 = {0, x - 1}, m2 = {y, x};
    auto [y2, z2] = atcoder::crt(r2, m2);
    if (z2 != 0) {
      if (y2 == 0) y2 = z2;
      chmin(ans, y2);
    }
  }
  cout << ans << endl;
}
