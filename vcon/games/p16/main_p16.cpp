using namespace std;

vector<vector<optional<bool>>> memo;

bool solve(int n, int p) {
  assert(n >= 0);
  assert(p >= 0);
  if (n == 0) return false;
  if (n <= p) return true;
  if (memo[n][p]) {
    return *memo[n][p];
  }
  for (int i = 1; i <= p; ++i) {
    if (not solve(n - i, i + 1)) {
      memo[n][p] = true;
      return true;
    }
  }
  memo[n][p] = false;
  return false;
}

int main() {
  std::ios::sync_with_stdio(false), cin.tie(nullptr);
  int n = in, P = in;
  memo.assign(n + 1, vector<optional<bool>>(n + 1));
  print(solve(n, P));
}
