template<typename T>
struct Array2d {
  std::vector<T> data_;
  size_t h_, w_;

  Array2d(size_t h, size_t w) : data_(h * w, T{}), h_(h), w_(w) {}

  inline T &get(size_t i0, size_t i1) { return data_[i0 * w_ + i1]; }

  // Fills one row with the specified value.
  inline void fill(size_t i0, T val) {
    std::fill(data_.begin() + i0 * w_, data_.begin() + (i0 + 1) * w_, val);
  }
};

template<class T>
using MinHeap = priority_queue<T, vector<T>, greater<T>>;

auto solve() {
  const int n = in;
  const int n2 = n * 2;
  vector<Int> V = in(n2);
  MinHeap<Int> heap;
  Int s = 0;
  for (int i = 1; i <= n; ++i) {
    int l = n - i, r = n + i - 1;
    heap.push(V[l]);
    heap.push(V[r]);
    s += heap.top();
    heap.pop();
  }
  return accumulate(ALL(V), 0LL) - s;
}

