using namespace std;

struct UnionFind {
  int n;
  mutable std::vector<int> par;  // positive: parent, negative: size
  int num_roots;

  explicit UnionFind(int sz) : n(sz), par(sz, -1), num_roots(sz) {}

  bool unite(int x, int y) {
    x = find(x), y = find(y);
    if (x == y) return false;
    if (par[x] > par[y]) std::swap(x, y);
    par[x] += par[y];
    par[y] = x;
    --num_roots;
    return true;
  }

  int find(int k) const {
    if (par[k] < 0) return k;
    return par[k] = find(par[k]);
  }

  int size(int k) const { return -par[find(k)]; }

  bool same(int x, int y) const { return find(x) == find(y); }

  std::vector<int> roots() const {
    std::vector<int> res;
    res.reserve(num_roots);
    for (int i = 0; i < (int)par.size(); ++i) {
      if (par[i] < 0) res.push_back(i);
    }
    return res;
  }
};

i64 solve() {
  int n, m, k;
  cin >> n >> m >> k;
  vector<int> c(k);
  REP(i, k) {
    cin >> c[i];
    --c[i];
  }
  UnionFind uf(n);
  REP(i, m) {
    int u, v;
    cin >> u >> v;
    --u, --v;
    uf.unite(u, v);
  }
  auto roots = uf.roots();
  set<int> used_roots;
  int maxr = 0;
  int maxi = -1;
  REP(i, k) {
    int r = uf.find(c[i]);
    if (chmax(maxr, uf.size(r))) {
      maxi = r;
    }
    used_roots.insert(r);
  }
  assert(maxi >= 0);
  for (auto r : roots) {
    if (used_roots.count(r)) continue;
    uf.unite(maxi, r);
  }

  i64 es = 0;
  for (auto r : uf.roots()) {
    i64 x = uf.size(r);
    es += x * (x - 1) / 2;
  }
  return es - m;
}

int main() {
  ios::sync_with_stdio(false), cin.tie(nullptr);
  cout << solve() << "\n";
}
