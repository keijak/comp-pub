using namespace std;
template <unsigned int M>
struct ModInt {
  constexpr ModInt(long long val = 0) : _v(0) {
    if (val < 0) {
      long long k = (abs(val) + M - 1) / M;
      val += k * M;
    }
    assert(val >= 0);
    _v = val % M;
  }

  static constexpr int mod() { return M; }
  static constexpr unsigned int umod() { return M; }
  inline unsigned int val() const { return _v; }

  ModInt &operator++() {
    _v++;
    if (_v == umod()) _v = 0;
    return *this;
  }
  ModInt &operator--() {
    if (_v == 0) _v = umod();
    _v--;
    return *this;
  }
  ModInt operator++(int) {
    auto result = *this;
    ++*this;
    return result;
  }
  ModInt operator--(int) {
    auto result = *this;
    --*this;
    return result;
  }

  constexpr ModInt operator-() const { return ModInt(-_v); }
  constexpr ModInt &operator+=(const ModInt &a) {
    if ((_v += a._v) >= M) _v -= M;
    return *this;
  }
  constexpr ModInt &operator-=(const ModInt &a) {
    if ((_v += M - a._v) >= M) _v -= M;
    return *this;
  }
  constexpr ModInt &operator*=(const ModInt &a) {
    _v = ((unsigned long long)(_v)*a._v) % M;
    return *this;
  }
  constexpr ModInt pow(unsigned long long t) const {
    ModInt base = *this;
    ModInt res = 1;
    while (t) {
      if (t & 1) res *= base;
      base *= base;
      t >>= 1;
    }
    return res;
  }

  constexpr ModInt inv() const {
    // Inverse by Extended Euclidean algorithm.
    // M doesn't need to be prime, but x and M must be coprime.
    // assert(_v != 0);
    // auto [g, x, deno] = ext_gcd(_v, M);
    // assert(g == 1LL);  // The GCD must be 1.
    // return x;

    // Inverse by Fermat's little theorem.
    // M must be prime. It's often faster.
    //
    return pow(M - 2);
  }
  constexpr ModInt &operator/=(const ModInt &a) { return *this *= a.inv(); }

  friend constexpr ModInt operator+(const ModInt &a, const ModInt &b) {
    return ModInt(a) += b;
  }
  friend constexpr ModInt operator-(const ModInt &a, const ModInt &b) {
    return ModInt(a) -= b;
  }
  friend constexpr ModInt operator*(const ModInt &a, const ModInt &b) {
    return ModInt(a) *= b;
  }
  friend constexpr ModInt operator/(const ModInt &a, const ModInt &b) {
    return ModInt(a) /= b;
  }
  friend constexpr bool operator==(const ModInt &a, const ModInt &b) {
    return a._v == b._v;
  }
  friend constexpr bool operator!=(const ModInt &a, const ModInt &b) {
    return a._v != b._v;
  }
  friend std::istream &operator>>(std::istream &is, ModInt &a) {
    return is >> a._v;
  }
  friend std::ostream &operator<<(std::ostream &os, const ModInt &a) {
    return os << a._v;
  }

 private:
  // Extended Euclidean algorithm
  // Returns (gcd(a,b), x, deno) where `a*x + b*deno == gcd(a,b)`.
  static std::tuple<int, int, int> ext_gcd(int a, int b) {
    int ax = 1, ay = 0, bx = 0, by = 1;
    for (;;) {
      if (b == 0) break;
      auto d = std::div(a, b);
      a = b;
      b = d.rem;
      ax -= bx * d.quot;
      std::swap(ax, bx);
      ay -= by * d.quot;
      std::swap(ay, by);
    }
    return {a, ax, ay};
  }

  unsigned int _v;  // raw value
};
const unsigned int MOD = 1'000'003;
using Mint = ModInt<MOD>;

pair<i64, i64> solve() {
  u64 n, k;
  cin >> n >> k;
  assert(k >= 2);
  if (n < 100 and u128(k) > (u128(1) << n)) {
    return {1, 1};
  }

  u128 p2 = 2;
  u64 d2 = 0;
  while (p2 <= u128(k - 1)) {
    d2 += (k - 1) / p2;
    p2 *= 2;
  }
  DEBUG(d2);
  Mint x = Mint(2).pow(n);
  Mint deno = x.pow(k - 1);
  deno /= Mint(2).pow(d2);

  if (k - 1 >= MOD) {
    return {deno.val(), deno.val()};
  }

  Mint nume = 1;
  for (u64 i = 1; i <= k - 1; ++i) {
    nume *= x - i;
  }
  nume /= Mint(2).pow(d2);
  return {(deno - nume).val(), deno.val()};
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  auto [a, b] = solve();
  cout << a << ' ' << b << endl;
}
