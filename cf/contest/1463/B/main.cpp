#define DEBUG(...)                                   \
  do {                                               \
    std::cerr << " \033[33m (L" << __LINE__ << ") "; \
    std::cerr << #__VA_ARGS__ << ":\033[0m ";        \
    pdebug(__VA_ARGS__);                             \
    std::cerr << std::endl;                          \
  } while (0)
#else
#define pdebug(...)
#define DEBUG(...)
#endif

using namespace std;

vector<i64> solve() {
  int n;
  cin >> n;
  vector<i64> a(n), b(n, 1), b2(n, 1);
  cin >> a;
  i64 delta1 = 0, delta2 = 0;
  REP(i, n) {
    if (i & 1) {
      b2[i] = a[i];
      b[i] = 1;
    } else {
      b[i] = a[i];
      b2[i] = 1;
    }
    delta1 += abs(a[i] - b[i]);
    delta2 += abs(a[i] - b2[i]);
  }

  i64 s = accumulate(ALL(a), 0LL);
  DEBUG(s);
  if (delta1 <= delta2) {
    assert(delta1 * 2 <= s);
    DEBUG(delta1, a, b);
    return b;
  }
  assert(delta2 * 2 <= s);
  DEBUG(delta2, a, b2);
  return b2;
}

int main() {
  std::ios::sync_with_stdio(false), cin.tie(nullptr);
  int t;
  cin >> t;
  REP(i, t) {
    auto b = solve();
    print_seq(b);
  }
}
