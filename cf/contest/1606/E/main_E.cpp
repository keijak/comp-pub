// mod: prime
template<class T = Mint>
struct Factorials {
  // factorials and inverse factorials.
  std::vector<T> fact, ifact;

  // n: max cached value.
  Factorials(int n) : fact(n + 1), ifact(n + 1) {
    assert(n >= 0);
    assert(n < T::mod());
    fact[0] = 1;
    for (int i = 1; i <= n; ++i) {
      fact[i] = fact[i - 1] * i;
    }
    ifact[n] = fact[n].inv();
    for (int i = n; i >= 1; --i) {
      ifact[i - 1] = ifact[i] * i;
    }
  }

  // Combination (nCk)
  T C(int n, int k) const {
    if (k < 0 || k > n) return 0;
    return fact[n] * ifact[k] * ifact[n - k];
  }
};

auto solve(int N, int X) -> Mint {
  Factorials fs(N);
  auto dp = vector(N + 1, vector(X + 1, Mint(0)));
  dp[N][0] = 1;
  for (int i = N; i >= 2; --i) {
    for (int d = 0; d < X; ++d) {
      if (dp[i][d].val() == 0) continue;
      for (int j = 0; j <= i; ++j) { // j heroes get killed in one round
        // d damage accumulated
        // will receive i-1 damage in this round
        // d < health <= min(d+i-1, X)
        Mint choice = min(d + i - 1, X) - d;
        int d2 = min(d + i - 1, X);
        dp[i - j][d2] += fs.C(i, j) * choice.pow(j) * dp[i][d];
      }
    }
  }
  Mint a0 = accumulate(ALL(dp[0]), Mint(0));
  Mint a1 = 0;
  REP(d, X) { a1 += (X - d) * dp[1][d]; }
  Mint tot = Mint(X).pow(N);
  check(tot.val() == (a0 + a1).val());
  return tot - a1;
}

