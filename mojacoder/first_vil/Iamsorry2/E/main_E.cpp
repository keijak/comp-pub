struct Clover {
  using T = array<int, 2>;
  static T op(const T &x, const T &y) {
    return {y[x[0]], y[x[1]]};
  }
  static constexpr T id() { return {0, 1}; }
};

auto solve() {
  cin >> S[0][0] >> S[0][1] >> S[1][0] >> S[1][1];
  const int n = in, Q = in;
  vector<int> a = in(n);

  auto tof = [&](int c) -> array<int, 2> {
    if (c == 0) {
      return {S[0][0], S[1][0]};
    } else {
      return {S[0][1], S[1][1]};
    }
  };

  vector<Clover::T> ini(n);
  REP(i, n) {
    ini[i] = tof(a[i]);
  }
  SegmentTree<Clover> st(ini);
  REP(qi, Q) {
    int t = in, x = in, y = in;
    if (t == 1) {
      --x;
      a[x] = y;
      st.set(x, tof(y));
    } else {
      --x;
      auto f = st.fold(x + 1, y);
      print(f[a[x]]);
    }
  }
}

