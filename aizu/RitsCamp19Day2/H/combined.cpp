using namespace std;

i64 solve() {
  int n, m;
  cin >> n >> m;
  string U;
  cin >> U;
  vector<int> a(n);
  cin >> a;

  atcoder::mf_graph<i64> g(n + 2);
  const int source = n, sink = n + 1;
  REP(i, n) {
    if (U[i] == 'R') {
      g.add_edge(source, i, a[i]);
      g.add_edge(i, sink, 0);
    } else {
      g.add_edge(source, i, 0);
      g.add_edge(i, sink, a[i]);
    }
  }
  REP(i, m) {
    int s, t, b;
    cin >> s >> t >> b;
    --s, --t;
    if (s > t) swap(s, t);
    g.add_edge(s, t, b);
  }
  i64 ans = g.flow(source, sink);
  return ans;
}

